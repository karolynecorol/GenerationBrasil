package Lista09_Atividade01;

public class Cliente {

	public String nome;
	public String telefone;
	public String cidade;
	
	
	public String nomeCliente (String cliente1) {
		nome = cliente1;
		return cliente1;
		}
	
	public String telefoneCliente (String cliente1) {
		telefone = cliente1;
		return cliente1;
		}
		
	public String cidadeCliente (String cliente1) {
		cidade = cliente1;
		return cliente1;
		}
	}


